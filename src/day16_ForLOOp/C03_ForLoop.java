package day16_ForLOOp;

public class C03_ForLoop {

	public static void main(String[] args) {
		
		
		  for(int i=10; i<=29;i++) {	
				System.out.print(i+", ");
				
				
		}
		  System.out.print(30);
		  System.out.println();
		
		for(int i=10; i<=30;i++) {
			if (i==30) {
				System.out.print(i);
			} else {
				System.out.print(i+", ");
			}
			
	   
		
		
	}
	}
}
