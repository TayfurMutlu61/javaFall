package day04_IncrementDecrement;

public class C06_Pre_IncrementPostIncrement {

	public static void main(String[] args) {
		
		
		int sayi1 =10;
		
		sayi1++;
		sayi1++;
		sayi1++;
		
		System.out.println(sayi1);
		
		
		System.out.println(sayi1++);
		
		System.out.println(sayi1);
		
		System.out.println(++sayi1);
		
		

	}

}
