package day04_IncrementDecrement;

public class C07_PreDecrementPostDecrement {

	public static void main(String[] args) {
		
		
		
		int sayi=10;
		
		sayi--;
		
		System.out.println(sayi);
		
		
		int a=sayi--;
		System.out.println(a);
		
		System.out.println(sayi);

		System.out.println(--a);
		
		
	}

}
